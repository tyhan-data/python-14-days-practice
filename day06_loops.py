# Day 6: Loops
# Task: For loop, while loop, loop through list

# For loop 1-10
print("For loop 1 to 10:")
for i in range(1, 11):
    print(i)

# While loop 1-10
print("While loop 1 to 10:")
n = 1
while n <= 10:
    print(n)
    n += 1

# Loop through list
sample_list = [3, 4, 6, 6, 34]
print("Looping through list:")
for item in sample_list:
    print(item)
